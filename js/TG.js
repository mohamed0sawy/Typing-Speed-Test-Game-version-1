const wordsArray = words.split("\n");

// to generate random number from wordsArray
function generateWord(arr){
    let num = Math.floor(Math.random() * 1000);
    return arr[num];
}


function main(){
    let mWord = generateWord(wordsArray);
    let cWord = generateWord(wordsArray);
    mainWord.innerHTML = mWord;
    comingWord.innerHTML = cWord;

    input.addEventListener("keyup",function(e){
        if(e.code === 'Space'){
            if(input.value === `${mWord} `){
                totalWords++;
                correctWords++;
                score.innerHTML = correctWords;
            } else {
                totalWords++;
            }
            input.value = '';
            mWord = cWord;
            cWord = generateWord(wordsArray);
            mainWord.innerHTML = mWord;
            comingWord.innerHTML = cWord;
        }
    });
}

main();

